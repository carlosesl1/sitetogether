import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const rootDir = process.cwd();
const postsPath = path.join(rootDir, "public", "data", "blog-posts.json");
const htaccessPath = path.join(rootDir, "public", ".htaccess");

const START_MARKER = "# BEGIN TOGETHER Legacy Blog Redirects";
const END_MARKER = "# END TOGETHER Legacy Blog Redirects";

function escapeRewritePattern(value) {
  return value.replace(/[\\^$.*+?()[\]{}|]/g, "\\$&");
}

function buildRedirectBlock(posts) {
  const slugs = [...new Set(posts.map((post) => post?.slug).filter(Boolean))].sort();
  const rules = slugs.flatMap((slug) => {
    const escapedSlug = escapeRewritePattern(slug);
    const canonicalPath = `/blog/${slug}`;

    return [
      `RewriteRule ^${escapedSlug}/?$ ${canonicalPath} [R=301,L,NE]`,
      `RewriteRule ^${escapedSlug}\\.html$ ${canonicalPath} [R=301,L,NE]`,
    ];
  });

  return [
    START_MARKER,
    "# Generated by scripts/update-legacy-blog-redirects.mjs. Do not edit by hand.",
    "# Legacy URLs are redirected to the canonical /blog/ URL to avoid duplicates.",
    ...rules,
    END_MARKER,
  ].join("\n");
}

function replaceBlock(htaccess, block) {
  const startIndex = htaccess.indexOf(START_MARKER);
  const endIndex = htaccess.indexOf(END_MARKER);

  if (startIndex !== -1 && endIndex !== -1 && endIndex > startIndex) {
    const afterEnd = endIndex + END_MARKER.length;
    return `${htaccess.slice(0, startIndex)}${block}${htaccess.slice(afterEnd)}`;
  }

  const insertBefore = "# Keep internal preview routes out of the public site.";
  const insertIndex = htaccess.indexOf(insertBefore);

  if (insertIndex === -1) {
    throw new Error(`Could not find insertion point in ${htaccessPath}`);
  }

  return `${htaccess.slice(0, insertIndex)}${block}\n\n${htaccess.slice(insertIndex)}`;
}

const posts = JSON.parse(await readFile(postsPath, "utf8"));
if (!Array.isArray(posts)) {
  throw new Error(`${postsPath} must contain an array of WordPress posts.`);
}

const htaccess = await readFile(htaccessPath, "utf8");
const nextHtaccess = replaceBlock(htaccess, buildRedirectBlock(posts));

await writeFile(htaccessPath, nextHtaccess, "utf8");
console.log(`Updated legacy blog redirects for ${posts.length} posts.`);
